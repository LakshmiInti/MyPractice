package training;

public class EncapsulationExample {
	private String name;  
	private int rollno;   
	public String getName(){  
	return name;  
	}  
	public void setName(String name){  
	this.name=name;
	}
	public int getRollNumber()
	{
		return rollno;
	}
	public void setRollNumber(int id)
	{
        rollno=id;		
	}
	
	}
